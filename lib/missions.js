export function getMissions({ page = 1, pageSize = 6 } = {}) {
  const safePageSize = Math.max(1, pageSize);
  const pinned = missions.filter((mission) => mission.pinned);
  const nonPinned = missions.filter((mission) => !mission.pinned);

  const totalItems = nonPinned.length;
  const totalPages = Math.max(1, Math.ceil(totalItems / safePageSize));
  const safePage = Math.min(Math.max(page, 1), totalPages);

  const start = (safePage - 1) * safePageSize;
  const end = start + safePageSize;

  const paginated = nonPinned.slice(start, end);

  return {
    pinned,
    missions: paginated,
    pagination: {
      page: safePage,
      pageSize: safePageSize,
      totalItems,
      totalPages,
      hasNextPage: safePage < totalPages,
      hasPreviousPage: safePage > 1,
    },
  };
}

export function getAllMissions() {
  return missions;
}

